#include <iostream>
#include <windows.h>
using namespace std;

// 1) Функция определения знака числа
void определитьЗнак(int num) {
    if (num > 0)   
    {
        cout << num << " - положительное число" << endl;
    }
    else if (num < 0) 
{
        cout << num << " - отрицательное число" << endl;
    }
    else 
    {
        cout << num << " - нулевое число" << endl;
    }
}

// 2) Функция вычисления среднего значения двух чисел
float среднееЗначение(int num1, int num2) 
{
    return static_cast<float>(num1 + num2) / 2;
}

// 3) Функция определения четности числа
void определитьЧетность(int num) 
{
    if (num % 2 == 0) 
    {
        cout << num << " - четное число" << endl;
    }
    else {
        cout << num << " - нечетное число" << endl;
    }
}

int main() 
{
    SetConsoleCP(1251);
    SetConsoleOutputCP(1251);

    int число1, число2;

    cout << "Введите первое число: ";
    cin >> число1;

    cout << "Введите второе число: ";
    cin >> число2;

    // Тестируем функции
    определитьЗнак(число1);
    определитьЗнак(число2);

    cout << "Среднее значение: " << среднееЗначение(число1, число2) << endl;

    определитьЧетность(число1);
    определитьЧетность(число2);

    return 0;
}